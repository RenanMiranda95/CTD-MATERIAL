module.exports.somar = function(n1, n2)
{
    return n1 + n2;
}

module.exports.multiplicar = function(n1, n2)
{
    return n1 * n2;
}

module.exports.subtrair = function(n1, n2)
{
    return n1 - n2;
}

module.exports.dividir = function(n1, n2)
{
    return n1 / n2;
}

module.exports.resto = function resto(n1, n2)
{
    return n1 % n2;
}